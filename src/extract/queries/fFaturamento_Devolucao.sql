SELECT
    SD1.D1_FILIAL,
    SD1.D1_ITEM,
    SD1.D1_DOC,
    SD1.D1_COD,
    SD1.D1_UM,
    SD1.D1_QUANT,
    SD1.D1_VUNIT,
    SD1.D1_TOTAL,
    SD1.D1_VALDESC,
    SD1.D1_ITEMORI,
    SD1.D1_NFORI,
    SD1.D1_FORNECE,
    SD1.D1_LOJA,
    SD1.D1_SERIE,
    SD1.D1_COR,
    SD1.D1_CHASSI,
    SD1.D1_EMISSAO,
    SD1.D1_DTDIGIT,
    SD1.D1_CF,
    SD1.D1_TIPO,
    SF1.F1_FILIAL,
    SF1.F1_DOC,
    SF1.F1_SERIE,
    SF1.F1_FORNECE,
    SF1.F1_LOJA,
    SF1.F1_FORMUL,
    SF1.F1_STATUS
FROM SD1010 SD1
INNER JOIN SF1010 SF1
    ON SF1.D_E_L_E_T_ = ''
   AND SF1.F1_FILIAL  = SD1.D1_FILIAL
   AND SF1.F1_DOC     = SD1.D1_DOC
   AND SF1.F1_SERIE   = SD1.D1_SERIE
   AND SF1.F1_FORNECE = SD1.D1_FORNECE
   AND SF1.F1_LOJA    = SD1.D1_LOJA
WHERE SD1.D_E_L_E_T_ = ''