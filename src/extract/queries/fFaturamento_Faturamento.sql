SELECT
    SF2.F2_FILIAL,
    SF2.F2_DOC,
    SF2.F2_CLIENTE,
    SF2.F2_LOJA,
    SF2.F2_COND,
    SF2.F2_DUPL,
    SF2.F2_EST,
    SF2.F2_TIPOCLI,
    SF2.F2_TIPO,
    SF2.D_E_L_E_T_ as SF2_D_E_L_E_T_,

    SD2.D2_FILIAL,
    SD2.D2_ITEM,
    SD2.D2_COD,
    SD2.D2_QUANT,
    SD2.D2_UM,
    SD2.D2_TOTAL,
    SD2.D2_DESCZFR,
    SD2.D2_CF,
    SD2.D2_CLIENTE,
    SD2.D2_LOJA,
    SD2.D2_DOC,
    SD2.D2_SERIE,
    SD2.D2_EMISSAO,
    SD2.D2_PEDIDO,
    SD2.D2_PICM,
    SD2.D2_ALQPIS,
    SD2.D2_ALQCOF,
    SD2.D2_ICMSRET
FROM SD2010 SD2
LEFT JOIN SF2010 SF2
    ON SF2.D_E_L_E_T_ = ''
   AND SD2.D2_FILIAL  = SF2.F2_FILIAL
   AND SD2.D2_DOC     = SF2.F2_DOC
   AND SD2.D2_CLIENTE = SF2.F2_CLIENTE
   AND SD2.D2_LOJA    = SF2.F2_LOJA
WHERE SD2.D_E_L_E_T_ = ''
  AND (SF2.D_E_L_E_T_ = '' OR SF2.D_E_L_E_T_ IS NULL)